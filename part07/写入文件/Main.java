public class Main {

    public static void main(String[] args) {
        // 文件字节输出流
        FileOutputStream out = null;
        try {
            // 创建File对象
            File file = new File("/Users/admin/Downloads/人人都是程序员.txt");
            // 当文件不存在时
            if (!file.exists()) {
                // 创建文件
                file.createNewFile();
            }
            // 初始化文件字节输出流
            out = new FileOutputStream(file);
            // 要写入的字符串
            String str = "abcdefgh";
            // 转为字节数组
            byte[] bytes = str.getBytes();
            // 将数据输出到文件中
            out.write(bytes);
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            // 当输出流不为空时
            if (out != null) {
                try {
                    // 关闭流
                    out.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
    }
}